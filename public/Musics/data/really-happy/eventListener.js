module.exports = async (type, { noteClickAuthor, note, notes, listenerState }, state) => {
    switch (type) {
        case 'noteClick':
            if (noteClickAuthor == 'player' && note?.type == 'hurtNote' && !notes?.find(n => n.type == 'normal')) {
                note.clicked = true
                state.musicInfo.combo = 0
                state.musicInfo.accuracyMedia.push(1)
                state.musicInfo.misses += 1
                state.musicInfo.score -= 50
                state.musicInfo.health -= 15
            }
            break
        case 'started':
            let oldCurrentTime = 0
            let loop = setInterval(() => {
                let currentTime = state.music?.currentTime

                for (let i in events) {
                    let event = events[i]

                    if (oldCurrentTime*1000 <= event[0] && currentTime*1000 >= event[0]) {
                        let current = 0
                        let interval = setInterval(() => {
                            if (current >= 10) {
                                state.screenXMovement = 0
                                state.screenYMovement = 0
                                state.screenZoom = 0
                                clearInterval(interval)
                            } else {
                                current += 1
                                if (event[1] == 'Screen Shake') {
                                    state.screenXMovement = Number.parseInt(Math.random()*80)-40
                                    state.screenYMovement = Number.parseInt(Math.random()*80)-40
                                } else if (event[1] == 'Add Camera Zoom') {
                                    state.screenZoom += 5
                                } else if (event[1] == 'Add Jumpscare') {
                                    state.musicInfo.jumpscareImage = {
                                        dir: `jumpscares/sadmouse-jumpscare-{{frame}}.png`,
                                        random: true,
                                        frame: 0,
                                        minFrame: 0,
                                        maxFrame: 2
                                    }
                                } else if (event[1] == 'Remove Jumpscare') {
                                    state.musicInfo.jumpscareImage = null
                                }
                            }
                        }, 1000/50)
                    }
                }

                oldCurrentTime = currentTime
                if (state.music?.duration <= state.music?.currentTime || state.gameStage != 'game') {
                    clearInterval(loop)
                    state.screenXMovement = 0
                    state.screenYMovement = 0
                    state.screenZoom = 0
                }
            }, 1000/50)
            break
    }
}

